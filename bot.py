def calc_totals(day_menu):
    total_kcal = total_b = total_j = total_u = 0
    for items in day_menu.values():
        for item in items:
            _, _, kcal, b, j, u = item
            total_kcal += kcal
            total_b += b
            total_j += j
            total_u += u
    return round(total_kcal), round(total_b, 1), round(total_j, 1), round(total_u, 1)

def format_menu(week, menu_num):
    days_label = "Понедельник – Среда" if menu_num == 1 else "Четверг – Суббота"
    menu = MENUS[week][menu_num]
    lines = []
    if menu_num == 1:
        lines.append("👑 Добрый день, моя королева!")
        lines.append("")
        lines.append("👨‍🍳 Я приготовил для вас меню на неделю.")
        lines.append("")
    lines.append(f"📅 *Неделя {week}  |  {days_label}*")
    lines.append("━" * 30)
    for meal_name, items in menu.items():
        if not items:
            continue
        lines.append(f"\n*{meal_name}*")
        for name, weight, kcal, b, j, u in items:
            lines.append(f"  • {name} — {weight}")
            lines.append(f"    _{kcal} ккал  |  Б {b}г  Ж {j}г  У {u}г_")
    kcal, b, j, u = calc_totals(menu)
    lines.append("")
    lines.append("━" * 30)
    lines.append(f"📊 *Итого за день:*")
    lines.append(f"🔥 *{kcal} ккал*  |  💪 Б: {b}г  🥑 Ж: {j}г  🍞 У: {u}г")
    if menu_num == 2:
        lines.append("")
        lines.append("🌸 *Приятного аппетита, моя королева!*")
        lines.append("Удачной недели и стройной фигуры! 💃")
    return "\n".join(lines)

def get_week_number():
    start = datetime(2025, 1, 6, tzinfo=pytz.timezone(TIMEZONE))
    now = datetime.now(pytz.timezone(TIMEZONE))
    delta_weeks = (now.date() - start.date()).days // 7
    return (delta_weeks % 5) + 1

async def send_weekly_menu(bot):
    week = get_week_number()
    for menu_num in [1, 2]:
        text = format_menu(week, menu_num)
        await bot.send_message(chat_id=CHAT_ID, text=text, parse_mode="Markdown")

async def cmd_start(update, context):
    await update.message.reply_text(
        "👨‍🍳 *Шеф Павел* на связи!\n\nКаждый понедельник в 10:00 вы получаете меню на всю неделю.\n\n📌 Команды:\n/menu — меню на текущую неделю\n/today — меню на сегодня",
        parse_mode="Markdown"
    )

async def cmd_menu(update, context):
    week = get_week_number()
    for menu_num in [1, 2]:
        text = format_menu(week, menu_num)
        await update.message.reply_text(text, parse_mode="Markdown")

async def cmd_today(update, context):
    week = get_week_number()
    tz = pytz.timezone(TIMEZONE)
    weekday = datetime.now(tz).weekday()
    menu_num = 1 if weekday <= 2 else 2
    text = format_menu(week, menu_num)
    await update.message.reply_text(text, parse_mode="Markdown")

async def main():
    app = ApplicationBuilder().token(TELEGRAM_TOKEN).build()
    app.add_handler(CommandHandler("start", cmd_start))
    app.add_handler(CommandHandler("menu", cmd_menu))
    app.add_handler(CommandHandler("today", cmd_today))
    tz = pytz.timezone(TIMEZONE)
    scheduler = AsyncIOScheduler(timezone=tz)
    scheduler.add_job(send_weekly_menu, trigger="cron", day_of_week="mon", hour=10, minute=0, args=[app.bot])
    scheduler.start()
    print("✅ Шеф Павел запущен!")
    await app.run_polling()

if __name__ == "__main__":
    asyncio.run(main())
